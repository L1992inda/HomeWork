package io.codelex.classesandobjects.practice;

public class SwapPoints {

    private int pointX = 0;
    private int pointY = 0;

    public SwapPoints(int x, int y) {
        this.pointX = x;
        this.pointY = y;

    }

    public int getPointX() {
        return pointX;
    }

    public int getPointY() {
        return pointY;
    }


}
